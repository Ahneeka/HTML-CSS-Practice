// Arrays:it deals with a list of objects  Array is an object in javascript N.B Array is a data structure used to represent a list of items
let selectedColors = ['red', 'blue', 'pink']; // the square bracket is array literal
selectedColors[3] = 'green'; // we added another element to the it so that it can expand 
selectedColors[4] = 'yellow';  // N.B the length in the array is dynamic it can change
console.log(selectedColors.length); // if you want to access an element in the array console.log(selectedColors[0])

// N.B array has a lot of properties that we can access using the dot notation e.g console.log(selectedColors.length or every,indexof,find,filter etc);