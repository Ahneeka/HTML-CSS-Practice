// function: function are one of the fundamental building blocks in javascript. a functionn is basically a set of statements that performs a task or calculate/returns a value 

//  how to declare a function using a function keyword
function greet() {
    console.log("Hello my people!");
}

greet(); //how to call the function


// how to make it interesting function can have inputs
function greet(name, lastName){
    console.log("Hello my people!" +" " + "i'm" + " "+name + " " +lastName);
}

greet('Ahneeka','jas'); //the Ahneeka jas  here is an argument while name and lastName are parameter 
greet('mary','james'); 


